    void updateTable() {
        try {
            Statement stmt = ob.conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = stmt.executeQuery("select * from student where department = '" + departmentcb.getSelectedItem().toString() + "' and course = '" + coursecb.getSelectedItem().toString() + "'");
            while (rs.next()) {
                String rollno = rs.getString("rollno");
                String name = rs.getString("name");
                String email = rs.getString("email");
                String contact = rs.getString("contact");
                String address = rs.getString("address");
                boolean photo = rs.getInt("photo") == 1 ? true : false;
                al.add(new Student(rollno, name, email, contact, address, photo));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    void view() {
        al = new ArrayList<>();
        updateTable();
        if (al.size() == 0) {
            JOptionPane.showMessageDialog(this, "No Student associated with this course");
            studenttb.setVisible(false);
            jButton2.setVisible(false);
        } else {
            tm = new MyTableModel((ArrayList<Object>) (Object) al);
            studenttb.setModel(tm);
            tm.fireTableDataChanged();
            studenttb.setVisible(true);
            jButton2.setVisible(true);
        }
    }

