    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (studenttb.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "No Student selected");
        } else {
            String rollno = al.get(studenttb.getSelectedRow()).rollno;
            Statement stmt;
            try {
                stmt = ob.conn.createStatement();
                int rs = stmt.executeUpdate("delete from student where rollno = '" + rollno + "'");
                if (rs == 1) {
                    JOptionPane.showMessageDialog(this, "Selected Student has been deleted");
                    view();
                }
            } catch (SQLException ex) {
                Logger.getLogger(ViewFaculty.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

