    private void courseaddbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_courseaddbtActionPerformed
        if (departmentcb.getSelectedIndex() == 0 || coursetf.getText().equals("") || coursedescriptionta.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All Fields are mandatory");
        } else {
            try {
                Statement stmt = new DBConnect().conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                int rs = stmt.executeUpdate("insert into course values(\'" + coursetf.getText() + "\' , \'" + departmentcb.getSelectedItem().toString() + "\' , \'" + coursedescriptionta.getText() + "\')");
                if (rs == 1) {
                    JOptionPane.showMessageDialog(this, "Course Successfully added");
                    coursetf.setText("");
                    coursedescriptionta.setText("");
                    departmentcb.setSelectedIndex(0);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Course Already Exists");
            }
        }
    }//GEN-LAST:event_courseaddbtActionPerformed

